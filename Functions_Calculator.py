def multiply(num1, num2):
    return print(f"{num1} * {num2} = {num1 * num2}")

def divide(num1, num2):
    return print(f"{num1} / {num2} = {int(num1 / num2)}")

def add(num1, num2):
    return print(f"{num1} + {num2} = {num1 + num2}")

def minus(num1, num2):
    return print(f"{num1} - {num2} = {num1 - num2}")

# FUNCTION CALLS
multiply(20, 5)
divide(1000, 10)
add(65, 35)
minus(147, 47)

